# While loop
number = 20  # Initialization
while number <= 25:  # Setting condition
    print(number)
    number += 1  # Updation

# Decrementing
num = 10
while num >= 1:
    print(num)
    num -= 1

# For loop
languages = ["python", "PHP", "Kotlin", "Java"]
print(languages)
for x in languages:
    print(x)

# Range
for z in range (30, 36):
    print(z)

for y in range (1, 10, 2):
    print(y)


